public class HeadType {
    private String id;
    private String headTypeName;
    private String headTypeImage;

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getHeadTypeName() {
        return headTypeName;
    }

    public void setHeadTypeName(String headTypeName) {
        this.headTypeName = headTypeName;
    }

    public String getHeadTypeImage() {
        return headTypeImage;
    }

    public void setHeadTypeImage(String headTypeImage) {
        this.headTypeImage = headTypeImage;
    }
}
